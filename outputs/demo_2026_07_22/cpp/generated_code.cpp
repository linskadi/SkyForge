/* ====================================================================
 * State machine (from architecture)
 * ==================================================================== */
enum class FlightState : std::uint8_t { OnGround, InAir, Fault };

/* [REQ-001] [MISRA-Rule-8-9] */
class FlightStateMachine {
public:
    FlightState state() const noexcept { return state_; }

    void on_event(std::uint8_t event_id) noexcept {
        using enum FlightState;
        switch (state_) {
        case OnGround:
            if (event_id == 0U) state_ = InAir;  // takeoff
            break;
        case InAir:
            if (event_id == 1U) state_ = OnGround; // landing
            else if (event_id == 2U) state_ = Fault; // fault
            break;
        case Fault:
            if (event_id == 3U) state_ = OnGround; // reset
            break;
        default:
            break;
        }
    }

private:
    FlightState state_{FlightState::OnGround};
};


