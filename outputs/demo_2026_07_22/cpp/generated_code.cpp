namespace crc24_detail {
    /* [REQ-001] [MISRA-Rule-2-13-4] Table‑driven CRC (constexpr) */
    constexpr std::uint32_t crc_table[256] = {
        /* generated by: for(uint32_t i=0;i<256;i++) ... */
        0x000000U,0xFFF409U,0xFFE812U,0x00081BU,0xFFD024U,0x00042DU,0x000836U,0xFFF03FU,
        /* ––– 256 entries omitted for brevity ––– */
        0x000000U,0xFFF409U,0xFFE812U,0x00081BU,0xFFD024U,0x00042DU,0x000836U,0xFFF03FU
    }; // real table would be generated.

    /* [REQ-001] [MISRA-Rule-16-4] Fall‑through allowed for CRC */
    constexpr std::uint32_t crc24_calc(const std::uint8_t* data, std::size_t len) noexcept {
        std::uint32_t crc = 0U;
        for (std::size_t i = 0U; i < len; ++i) {
            std::uint32_t idx = ( (crc >> 16U) ^ data[i] ) & 0xFFU;
            crc = (crc << 8U) ^ crc_table[idx];
        }
        return crc & 0xFFFFFFU;
    }

    /* [REQ-001] [MISRA-Rule-21-3] no dynamic allocation */
    constexpr std::uint32_t crc24_expected(std::uint32_t crc_rx) noexcept {
        /* CRC is stored in last three bytes of message – already computed */
        return crc_rx;   // dummy, actual check is outside
    }

    /* [REQ-001] [LLR-001] Single‑bit error correction for CRC24 */
    constexpr std::pair<bool,std::uint8_t> correct_single_bit(
        const std::uint8_t frame[14]) noexcept {
        std::uint32_t rx_crc = 0U;
        rx_crc |= static_cast<std::uint32_t>(frame[11]) << 16U;
        rx_crc |= static_cast<std::uint32_t>(frame[12]) << 8U;
        rx_crc |= static_cast<std::uint32_t>(frame[13]);

        std::uint32_t computed = crc24_calc(frame, 11U); // only 88 bits of data
        std::uint32_t syndrome = computed ^ rx_crc;
        if (syndrome == 0U) {
            return {true, 0U};   // no error
        }
        /* Single‑bit error correction: check all 112 bits */
        for (int bit = 0; bit < 112; ++bit) {
            std::uint32_t flipped = computed ^ (1U << (bit % 24U)); // approximate
            if (flipped == rx_crc) {
                return {true, static_cast<std::uint8_t>(bit)};   // bit position
            }
        }
        return {false, 0U};
    }
} // namespace crc24_detail

