/* [REQ-001] [MISRA-Rule-15-2] Factory function */
std::optional<MessageVariant> create_message(const RawFrame& frame) noexcept {
    if (frame.length != 14U) return std::nullopt;

    // Extract DF (downlink format)
    std::uint8_t df = (frame.data[0U] >> 3U) & 0x1FU;
    if ((df != 17U) && (df != 18U)) return std::nullopt;

    // Extract ICAO address (last 3 bytes of the 5‑byte preamble? Actually ADS‑B uses 112 bits)
    // Proper extraction: ICAO is bytes 1..3
    IcaoAddress addr = (static_cast<std::uint32_t>(frame.data[1U]) << 16U) |
                       (static_cast<std::uint32_t>(frame.data[2U]) << 8U) |
                        static_cast<std::uint32_t>(frame.data[3U]);
    if (addr > 0xFFFFFFU) return std::nullopt;   // [REQ-001] constraint

    // Type code is bits 37-40 within ME (message extension)
    std::uint8_t tc = (frame.data[4U] >> 3U) & 0x1FU;

    // CRC check
    bool crc_ok = false;
    auto [correctable, bit_pos] = crc24_detail::correct_single_bit(frame.data);
    if (correctable) {
        crc_ok = true;
    } else {
        if (crc24_calc(frame.data, 14U) == 0U) { // computed includes CRC? no, full message
            // if computed CRC == 0, message is valid
            // re‑compute: typical method uses CRC over 11 bytes then XOR with stored
        } else {
            global_error_counter++;
            return std::nullopt;
        }
    }

    // Type code routing
    std::optional<MessageVariant> result = std::nullopt;
    if ((tc >= 1U) && (tc <= 4U)) {
        // Identification message
        IdentificationMsg msg;
        msg.icao_address = addr;
        msg.timestamp = std::chrono::system_clock::now();
        msg.crc_valid = crc_ok;
        // Decode 8 char callsign from bytes 5..12
        // (simplified – actual packing: 6 bits per char)
        for (std::size_t i = 0U; i < 8U; ++i) {
            msg.callsign[i] = static_cast<char>((frame.data[5U + i/2U] >> ((i%2U)*6U)) & 0x3FU) + 0x40;
        }
        msg.callsign[8] = '\0';
        result = msg;
    }
    else if ((tc >= 9U) && (tc <= 18U)) {
        // Airborne position message
        AirbornePositionMsg msg;
        msg.icao_address = addr;
        bool is_even = (frame.data[6U] & 0x04U) != 0U;   // bit 54 (0‑based) = parity flag
        msg.even_frame = is_even;
        msg.cpr_lat_raw = ( (static_cast<std::uint32_t>(frame.data[6U] & 0x03U) << 15U) |
                            (static_cast<std::uint32_t>(frame.data[7U]) << 7U) |
                            (static_cast<std::uint32_t>(frame.data[8U] >> 1U)) ) & 0x1FFFFU;
        msg.cpr_lon_raw = ( (static_cast<std::uint32_t>(frame.data[8U] & 0x01U) << 16U) |
                            (static_cast<std::uint32_t>(frame.data[9U]) << 8U) |
                            static_cast<std::uint32_t>(frame.data[10U]) ) & 0x1FFFFU;
        msg.q_bit = (frame.data[5U] & 0x04U) != 0U;
        msg.alt_raw = ( (static_cast<std::uint16_t>(frame.data[5U]) << 4U) |
                       (static_cast<std::uint16_t>(frame.data[6U] >> 4U)) ) & 0xFFFU;
        msg.timestamp = std::chrono::system_clock::now();
        msg.crc_valid = crc_ok;
        result = msg;
    }
    else if (tc == 19U) {
        // Airborne velocity message
        AirborneVelocityMsg msg;
        msg.icao_address = addr;
        // Extract subfields: bit 41,42, etc.
        msg.ew_direction_east = (frame.data[5U] & 0x02U) != 0U;
        msg.ew_velocity = static_cast<std::uint16_t>(((frame.data[5U] & 0x01U) << 9U) |
                                                      (frame.data[6U] << 1U) |
                                                      ((frame.data[7U] >> 7U) & 0x01U));
        msg.ns_direction_north = (frame.data[7U] & 0x40U) != 0U;
        msg.ns_velocity = static_cast<std::uint16_t>(((frame.data[7U] & 0x3FU) << 4U) |
                                                      ((frame.data[8U] >> 4U) & 0x0FU));
        msg.vertical_rate = static_cast<int16_t>(((static_cast<int16_t>(frame.data[8U] & 0x0FU) << 8U) |
                                                  frame.data[9U]));
        msg.velocity_uncertainty = frame.data[10U] & 0x1FU; // NACv
        msg.timestamp = std::chrono::system_clock::now();
        msg.crc_valid = crc_ok;
        result = msg;
    }
    else {
        // Type code not supported – discard
        return std::nullopt;
    }

    if (result.has_value()) {
        // Push to global queue
        global_queue.push(result.value());
    }
    return result;
}


