/* ====================================================================
 * Message queue – static array, time sorted, per‑ICAO grouping (simplified)
 * ==================================================================== */

/* [REQ-001] [MISRA-Rule-21-3] Static container */
static constexpr std::size_t QUEUE_CAPACITY = 256U;

/* [REQ-001] [MISRA-Rule-9-3-1] Entry type */
struct QueueEntry final {
    MessageVariant msg;
    std::chrono::steady_clock::time_point rx_time;
};

/* [REQ-001] [LLR-005] Queue manager */
class MessageQueue {
public:
    /* [REQ-001] [MISRA-Rule-15-2] */
    std::optional<MessageVariant> push(const MessageVariant& msg) noexcept {
        // 1) Remove expired messages (rx_time < now - 30s)
        expire_old();
        // 2) Check capacity
        if (size_ >= QUEUE_CAPACITY) {
            // queue full – discard
            fault_detected_ = true;
            return std::nullopt;
        }
        // 3) Insert in chronological order
        auto now = std::chrono::steady_clock::now();
        std::size_t pos = size_;
        for (std::size_t i = 0U; i < size_; ++i) {
            if (entries_[i].rx_time > now) {
                pos = i;
                break;
            }
        }
        // shift elements right
        for (std::size_t i = size_; i > pos; --i) {
            entries_[i] = entries_[i-1U];
        }
        entries_[pos] = QueueEntry{msg, now};
        ++size_;
        // 4) For position messages, attempt CPR pairing (global reference)
        if (std::holds_alternative<AirbornePositionMsg>(msg)) {
            // could trigger pairing – out of scope for this file
        }
        return msg;   // return the inserted message
    }

    /* [REQ-001] [MISRA-Rule-15-2] */
    std::optional<MessageVariant> front() const noexcept {
        if (size_ == 0U) return std::nullopt;
        return entries_[0U].msg;
    }

    void pop_front() noexcept {
        if (size_ > 0U) {
            for (std::size_t i = 0U; i < size_-1U; ++i) {
                entries_[i] = entries_[i+1U];
            }
            --size_;
        }
    }

    std::size_t size() const noexcept { return size_; }
    bool fault_detected() const noexcept { return fault_detected_; }

private:
    std::array<QueueEntry, QUEUE_CAPACITY> entries_{};
    std::size_t size_{0U};
    bool fault_detected_{false};

    /* [REQ-001] [MISRA-Rule-2-5] */
    void expire_old() noexcept {
        auto threshold = std::chrono::steady_clock::now() - std::chrono::seconds(30);
        std::size_t new_size = 0U;
        for (std::size_t i = 0U; i < size_; ++i) {
            if (entries_[i].rx_time >= threshold) {
                if (i != new_size) {
                    entries_[new_size] = entries_[i];
                }
                ++new_size;
            }
        }
        size_ = new_size;
    }
};

